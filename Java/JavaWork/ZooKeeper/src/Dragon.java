public class Dragon extends Mammal{
	
	public Dragon(){
		this.energyLevel = 300;
	}
	
	public void fly(){
		System.out.println("The dragon is flying!");
		this.energyLevel -= 50;
	}
	public void eatHumans(){
		System.out.println("The dragon is eating humans!");
		this.energyLevel += 25;
	}
	public void attackTown(){
		System.out.println("The dragon is attacking a town!");
		this.energyLevel -= 100;
	}
}